usuarioPalavra = input("Entre com uma palavra: ")
usuarioPalavra = usuarioPalavra.upper()

for letra in usuarioPalavra:
    if letra == "A":
        continue
    elif letra == "E":
        continue
    elif letra == "I":
        continue
    elif letra == "O":
        continue
    elif letra == "U":
        continue
    else:
        print(letra)